function showNav(){
    // tìm đén cái nav
    const headerNav = document.getElementById('header-nav')

    // thêm active vào class
    headerNav.classList.add('active');

}
function hiddenNav(){
    // tìm đén cái nav
    const headerNav = document.getElementById('header-nav')

    // thêm active vào class
    headerNav.classList.remove('active');
}
